package Pratices;

public class Hari1_loops 
{

	public static void main(String[] args) 
	{
		int n=1000;
		int i=10;
		for(i=10; i<=n;i++)
		{
			if(i%2==0)
			{
				System.out.println(i+"it is even number");
			}
		
			else if(i%2!=0)
			{
				System.out.println(i+"it is a odd number");
			
			}
	
			System.out.println(i);
				
			}
		}

	

}

