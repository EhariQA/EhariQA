package p1;

public class relationaloperators {

	public static void main(String[] args) {
		//relational operators(==)
		int d = 50;
		int g  = 40;
		System.err.println(d==g);
		System.err.println(d>g);
		System.err.println(d!=g);
		System.err.println(d<=g);
		System.err.println(d>=g);
	}  

}
